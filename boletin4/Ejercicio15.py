dia = int(input("Introduce el dia de la semana de forma numérica: "))

if dia == 1:
    print("El día indicado es Lunes")
elif dia == 2:
    print("El día indicado es Martes")    
elif dia == 3:
    print("El día indicado es Miércoles")
elif dia == 4:
    print("El día indicado es Jueves")
elif dia == 5:
    print("El día indicado es Viernes")
elif dia == 6:
    print("El día indicado es Sábado")
elif dia == 7:
    print("El día indicado es Domingo")
else:
    print("Los datos indicados son incorrectos")